  <h1>Om denne appen</h1>

  <p>
    Denne appen gir informasjon om fartsgrenser. Fargekodene angir følgende: <strong
      >grønn</strong
    >
    betyr at du kjører under fartsgrensen, <strong>gul</strong> indikerer at du
    kjører over fartsgrensen, og <strong>rød</strong> advarer om at hastigheten
    er så høy at du risikerer å miste førerkortet. Merk at fartsgrensene er
    basert på data fra
    <a
      href="https://lovdata.no/dokument/SF/forskrift/2003-12-19-1660/KAPITTEL_2#%C2%A72-2"
      >Lovdata</a
    >.
  </p>

  <p>
    Vær oppmerksom på GPS-unøyaktighet og bruk appen som en veiledning, ikke en
    absolutt indikator. Vi oppfordrer ikke til å bryte fartsgrensene, og vi tar
    ikke ansvar for eventuelle feil i applikasjonen, utdaterte regler, eller
    annen feilinformasjon. Bruk alltid skjønn og kjør forsiktig.
  </p>
